void adc_init(uint16_t *buf, uint16_t data_len)
{
  
  if (!buf) {
	sys_log("%s,Invalid parameter\r\n",__FUNCTION__);
	while(1);
  }

  //MX_DMA_Init();
  MX_ADC1_Init();
  /* adc dma init */
  LL_DMA_SetMode(DMA1, LL_DMA_CHANNEL_1, LL_DMA_MODE_CIRCULAR);
  LL_DMA_SetDataLength(DMA1, LL_DMA_CHANNEL_1, data_len);
  LL_DMA_SetPeriphAddress(DMA1, LL_DMA_CHANNEL_1, LL_ADC_DMA_GetRegAddr(ADC1, LL_ADC_DMA_REG_REGULAR_DATA));
  LL_DMA_SetMemoryAddress(DMA1, LL_DMA_CHANNEL_1, (uint32_t)buf);
  LL_DMA_EnableChannel(DMA1, LL_DMA_CHANNEL_1);

  LL_ADC_Enable(ADC1);
  /* adc configure */
  LL_ADC_StartCalibration(ADC1);
  while(LL_ADC_IsCalibrationOnGoing(ADC1));

  LL_ADC_REG_SetDMATransfer(ADC1, LL_ADC_REG_DMA_TRANSFER_UNLIMITED);
//  LL_ADC_REG_StartConversionSWStart(ADC1);
}

void phy_adc_start(void)
{
  LL_ADC_REG_StartConversionSWStart(ADC1);
}

void phy_adc_stop(void)
{
	LL_ADC_REG_StopConversionExtTrig(ADC1);
}
